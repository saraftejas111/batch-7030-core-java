package com.loops;

public class BasicWhile {

	public static void main(String[] args) {

		int num = 1; // start

		while (num <= 10) { // end
			System.out.println(num);
			num++; // how
		}

		System.out.println();

		int rev = 10;

		while (rev >= 1) {
			System.out.println(rev);
			rev--;
		}

	}
}
